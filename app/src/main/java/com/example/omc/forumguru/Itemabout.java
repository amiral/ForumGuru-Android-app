package com.example.omc.forumguru;

public class Itemabout {

    private String description,s,m;

    public Itemabout( String description,String s,String m) {
        super();

        this.description = description;
        this.s=s;
        this.m=m;
    }


    public String getDescription() {
        return description;
    }
    public String gets() {
        return s;
    }
    public String getm() {
        return m;
    }
}


